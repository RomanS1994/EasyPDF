export function getPageMetaKey() {
  const shell = document.body?.dataset?.appShell;
  const tab = document.body?.dataset?.appTab;
  if (!shell || !tab) return null;

  if (shell === 'admin' && tab === 'settings') {
    const settingsPage = document.body?.dataset?.appSettingsPage || 'hub';
    return settingsPage === 'hub' ? shell + '.' + tab : shell + '.' + tab + '.' + settingsPage;
  }

  return shell + '.' + tab;
}
