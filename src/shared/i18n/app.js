translations.en = english;
translations.cs = czech;

function resolveLanguage(value) {
  return SUPPORTED_LANGUAGES.includes(value) ? value : DEFAULT_LANGUAGE;
}

export function getStoredLanguage() {
  return resolveLanguage(localStorage.getItem(LANGUAGE_STORAGE_KEY) || DEFAULT_LANGUAGE);
}

export function setStoredLanguage(language) {
  const nextLanguage = resolveLanguage(language);
  localStorage.setItem(LANGUAGE_STORAGE_KEY, nextLanguage);
  return nextLanguage;
}

export function getCurrentLanguage() {
  return resolveLanguage(document.documentElement.lang || getStoredLanguage());
}

export function getCurrentLocale() {
  const language = getCurrentLanguage();
  if (language === 'en') return 'en-GB';
  if (language === 'cs') return 'cs-CZ';
  return 'uk-UA';
}

export function formatMessage(template, params = {}) {
  return Object.entries(params).reduce((result, [key, value]) => {
    return result.replaceAll(`{${key}}`, String(value ?? ''));
  }, template);
}

export function t(key, params = {}, language = getCurrentLanguage()) {
  const nextLanguage = resolveLanguage(language);
  const template =
    translations[nextLanguage]?.[key] ??
    translations[DEFAULT_LANGUAGE]?.[key] ??
    key;

  return params && typeof template === 'string'
    ? formatMessage(template, params)
    : template;
}

function applyTextTranslations(root = document) {
  root.querySelectorAll('[data-i18n]').forEach(element => {
    const key = element.dataset.i18n;
    if (!key) return;
    element.textContent = t(key);
  });

  root.querySelectorAll('[data-i18n-placeholder]').forEach(element => {
    const key = element.dataset.i18nPlaceholder;
    if (!key) return;
    element.setAttribute('placeholder', t(key));
  });

  root.querySelectorAll('[data-i18n-title]').forEach(element => {
    const key = element.dataset.i18nTitle;
    if (!key) return;
    element.setAttribute('title', t(key));
  });

  root.querySelectorAll('[data-i18n-aria-label]').forEach(element => {
    const key = element.dataset.i18nAriaLabel;
    if (!key) return;
    element.setAttribute('aria-label', t(key));
  });
}

function getPageMetaKey() {
  const shell = document.body?.dataset?.appShell;
  const tab = document.body?.dataset?.appTab;
  if (!shell || !tab) return null;

  if (shell === 'admin' && tab === 'settings') {
    const settingsPage = document.body?.dataset?.appSettingsPage || 'hub';
    return settingsPage === 'hub' ? `${shell}.${tab}` : `${shell}.${tab}.${settingsPage}`;
  }

  return `${shell}.${tab}`;
}

function applyPageMeta(language = getCurrentLanguage()) {
  const metaKey = getPageMetaKey();
  const meta = metaKey ? PAGE_META[metaKey] : null;
  if (!meta) return;

  document.title = meta.title?.[language] || meta.title?.[DEFAULT_LANGUAGE] || document.title;

  const description = document.querySelector('meta[name="description"]');
  if (description) {
    description.setAttribute(
      'content',
      meta.description?.[language] ||
        meta.description?.[DEFAULT_LANGUAGE] ||
        description.getAttribute('content') ||
        ''
    );
  }
}

export function syncPageMeta(language = getCurrentLanguage()) {
  applyPageMeta(language);
}

export function applyLanguage(language = getStoredLanguage(), { persist = true } = {}) {
  const nextLanguage = persist ? setStoredLanguage(language) : resolveLanguage(language);

  document.documentElement.lang = nextLanguage;
  applyTextTranslations(document);
  applyPageMeta(nextLanguage);

  window.dispatchEvent(
    new CustomEvent('pdf-app:language-changed', {
      detail: { language: nextLanguage },
    })
  );

  return nextLanguage;
}

export function initLanguage() {
  const language = getStoredLanguage();
  document.documentElement.lang = language;
  applyTextTranslations(document);
  applyPageMeta(language);
  return language;
}
